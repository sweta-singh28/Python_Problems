numbers = [2,1,3,4]
print(numbers)
numbers.sort()  #sorts in ascending order
print(numbers)
numbers.sort(reverse = True)   #sorts in descending order
print(numbers)
numbers.reverse()  #reverses the list
print(numbers)

numbers.append(9)   #adds one element at the end
print(numbers)

numbers.insert(2, 8)    #insert element at index
print(numbers) 


