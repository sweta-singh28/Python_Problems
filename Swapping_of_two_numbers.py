# Swapping of two numbers
# a = 2
# b = 3
a = input("Enter the value of a: ")
b = input("Enter the value of b: ")
print("Before swapping")
print("a = ", a)
print("b = ", b) 
print("After swapping")
temp = a        #temp = 2
a =  b          #a = 3
b = temp        #b = 2
print("a = ", a)
print("b = ", b)